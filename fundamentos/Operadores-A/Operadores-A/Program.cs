using System;

namespace Operadores_A
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int num1 = 5;
            int num2 = 6;

            //Soma
            Console.WriteLine(num1 + num2);
            //Subtração
            Console.WriteLine(num1 - num2);
            // Multiplicação
            Console.WriteLine(num1 * num2);
            // Divisão
            Console.WriteLine(num1 / num2);
            // Resto
            Console.WriteLine( num1 % 2);
            Console.ReadKey();
        }
    }
}